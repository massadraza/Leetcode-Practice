class Solution:
    def trap(self, height: List[int]) -> int:
        left = 0
        right = len(height) - 1

        leftMax = height[left]
        rightMax = height[right]
        result = 0

        while left < right:
            if leftMax < rightMax: 
                left = left + 1
                leftMax = max(leftMax, height[left])
                result = result + leftMax - height[left]
            else:
                right = right - 1
                rightMax = max(rightMax, height[right])
                result = result + rightMax - height[right]
            
        return result